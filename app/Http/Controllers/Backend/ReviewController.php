     * @return \Illuminate\Http\Response
     */
    public function status($id){
        $review = Review::findOrFail($id);
        if($review){
            if($review->status == 0){
                $review->status = 1;
            }else{
                $review->status = 0;
            }
            $review->save();
            Session()->flash('alert-success','Status Addedd Successfully');
            return back();
        }else{
           return abort(404);
        }
    }
